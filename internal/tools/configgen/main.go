package main

import (
	"bytes"
	"encoding/json"
	"flag"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"runtime"
	"sort"
	"strings"

	"github.com/Yacobolo/leapview/internal/configspec"
)

func main() {
	check := flag.Bool("check", false, "fail when generated files differ from the catalog")
	flag.Parse()
	outputs, err := generatedOutputs()
	if err != nil {
		fatal(err)
	}
	for path, content := range outputs {
		if *check {
			existing, err := os.ReadFile(path)
			if err != nil || !bytes.Equal(existing, content) {
				fatal(fmt.Errorf("generated configuration artifact is stale: %s", path))
			}
			continue
		}
		if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
			fatal(err)
		}
		if err := os.WriteFile(path, content, 0o644); err != nil {
			fatal(err)
		}
	}
}

func generatedOutputs() (map[string][]byte, error) {
	goSource, err := generateGo()
	if err != nil {
		return nil, err
	}
	schema, err := generateSchema()
	if err != nil {
		return nil, err
	}
	root := repositoryRoot()
	return map[string][]byte{
		filepath.Join(root, "internal/config/config_gen.go"):          goSource,
		filepath.Join(root, "internal/configspec/names_gen.go"):       generateNames(),
		filepath.Join(root, "docs/configuration.md"):                  generateDocs(),
		filepath.Join(root, ".env.example"):                           generateEnvExample(),
		filepath.Join(root, "schemas/config/environment.schema.json"): schema,
	}, nil
}

func repositoryRoot() string {
	_, file, _, ok := runtime.Caller(0)
	if !ok {
		fatal(fmt.Errorf("locate config generator source"))
	}
	return filepath.Clean(filepath.Join(filepath.Dir(file), "..", "..", ".."))
}

func generateGo() ([]byte, error) {
	var out strings.Builder
	out.WriteString("// Code generated by internal/tools/configgen; DO NOT EDIT.\n\npackage config\n\n")
	out.WriteString("import (\n\t\"time\"\n\n\t\"github.com/Yacobolo/leapview/internal/configspec\"\n)\n\n")
	out.WriteString("type Config struct {\n")
	for _, setting := range configspec.Settings() {
		if !setting.Runtime {
			continue
		}
		fmt.Fprintf(&out, "\t// %s %s\n", setting.Field, setting.Description)
		decodeType := setting.Type
		if setting.DecodeType != "" {
			decodeType = setting.DecodeType
		}
		fmt.Fprintf(&out, "\t%s %s `env:%q", setting.Field, goType(decodeType), setting.Name)
		if setting.Default != "" {
			fmt.Fprintf(&out, " envDefault:%q", setting.Default)
		}
		out.WriteString("`\n")
	}
	out.WriteString("}\n\n")
	out.WriteString("func (c Config) catalogValues() map[string]any {\n\treturn map[string]any{\n")
	for _, setting := range configspec.Settings() {
		if setting.Runtime {
			fmt.Fprintf(&out, "\t\tconfigspec.%s: c.%s,\n", "Env"+setting.Name, setting.Field)
		}
	}
	out.WriteString("\t}\n}\n")
	return format.Source([]byte(out.String()))
}

func generateNames() []byte {
	var out strings.Builder
	out.WriteString("// Code generated by internal/tools/configgen; DO NOT EDIT.\n\npackage configspec\n\nconst (\n")
	for _, setting := range configspec.Settings() {
		fmt.Fprintf(&out, "\tEnv%s = %q\n", setting.Name, setting.Name)
	}
	out.WriteString(")\n")
	formatted, err := format.Source([]byte(out.String()))
	if err != nil {
		fatal(err)
	}
	return formatted
}

func goType(valueType configspec.ValueType) string {
	switch valueType {
	case configspec.TypeBool:
		return "bool"
	case configspec.TypeInt:
		return "int"
	case configspec.TypeInt64:
		return "int64"
	case configspec.TypeDuration:
		return "time.Duration"
	default:
		return "string"
	}
}

func generateDocs() []byte {
	settings := configspec.Settings()
	var out strings.Builder
	out.WriteString("<!-- Code generated by internal/tools/configgen; DO NOT EDIT. -->\n\n# Environment variable reference\n\n")
	out.WriteString("LeapView configuration is process-global. Environment settings apply to the entire instance and cannot be overridden per workspace. Values marked secret must come from the deployment environment or a secret manager.\n\n")
	out.WriteString("Run `leapview config validate` to validate the active environment, or add `--production` to apply production requirements when `LEAPVIEW_PRODUCTION` is not set. Validation never prints configured values or secrets.\n\n")
	categories := []string{}
	seen := map[string]bool{}
	for _, setting := range settings {
		if !seen[setting.Category] {
			seen[setting.Category] = true
			categories = append(categories, setting.Category)
		}
	}
	sort.Strings(categories)
	for _, category := range categories {
		fmt.Fprintf(&out, "## %s\n\n| Environment variable | Type / default | Scope | Lifecycle | Description |\n|---|---|---|---|---|\n", title(category))
		for _, setting := range settings {
			if setting.Category != category {
				continue
			}
			typeDefault := string(setting.Type)
			if setting.Default != "" {
				typeDefault += " / `" + setting.Default + "`"
			}
			if setting.Secret {
				typeDefault += " / secret"
			}
			description := setting.Description
			if setting.AliasFor != "" {
				description += " Alias for `" + setting.AliasFor + "`."
			}
			fmt.Fprintf(&out, "| `%s` | %s | %s | %s | %s |\n", setting.Name, typeDefault, setting.Scope, setting.Lifecycle, description)
		}
		out.WriteString("\n")
	}
	out.WriteString("## Relationships\n\n")
	for _, rule := range configspec.Rules() {
		fmt.Fprintf(&out, "- **%s:** %s Failure: `%s`.\n", rule.ID, rule.Description, rule.Message)
	}
	return []byte(out.String())
}

func generateEnvExample() []byte {
	var out strings.Builder
	out.WriteString("# Code generated by internal/tools/configgen; DO NOT EDIT.\n")
	out.WriteString("# Valid local-auth production baseline. Replace placeholder secrets before use.\n")
	settings := configspec.Settings()
	sort.SliceStable(settings, func(i, j int) bool {
		if settings[i].Category == settings[j].Category {
			return settings[i].Name < settings[j].Name
		}
		return settings[i].Category < settings[j].Category
	})
	current := ""
	for _, setting := range settings {
		if !setting.Runtime || (setting.Lifecycle != "supported" && setting.Lifecycle != "development") || setting.AliasFor != "" {
			continue
		}
		if setting.Category != current {
			current = setting.Category
			fmt.Fprintf(&out, "\n# %s\n", title(current))
		}
		value := setting.EnvExample
		if value == "" {
			value = setting.Default
		}
		if value == "" && !setting.Secret {
			value = setting.Example
		}
		if setting.Secret && value == "" {
			value = "replace-with-secret"
		}
		prefix := ""
		if setting.Commented || setting.EnvExample == "" {
			prefix = "# "
		}
		fmt.Fprintf(&out, "%s%s=%s\n", prefix, setting.Name, value)
	}
	return []byte(out.String())
}

func generateSchema() ([]byte, error) {
	properties := map[string]any{}
	for _, setting := range configspec.Settings() {
		property := map[string]any{
			"type":        "string",
			"description": setting.Description,
			"x-scope":     setting.Scope,
			"x-lifecycle": setting.Lifecycle,
		}
		if setting.Default != "" {
			property["default"] = setting.Default
		}
		if setting.Secret {
			property["writeOnly"] = true
		}
		switch setting.Type {
		case configspec.TypeBool:
			property["pattern"] = `^(1|t|T|true|TRUE|True|0|f|F|false|FALSE|False)$`
		case configspec.TypeInt, configspec.TypeInt64:
			property["pattern"] = `^-?[0-9]+$`
		case configspec.TypeDuration:
			property["pattern"] = `^-?[0-9]+(ns|us|µs|ms|s|m|h)([0-9]+(ns|us|µs|ms|s|m|h))*$`
		}
		if setting.AliasFor != "" {
			property["deprecated"] = true
			property["x-alias-for"] = setting.AliasFor
		}
		properties[setting.Name] = property
	}
	allOf := []any{}
	for _, rule := range configspec.Rules() {
		entry := map[string]any{
			"description":     rule.Description,
			"then":            predicateSchema(rule.Assert),
			"x-error-message": rule.Message,
		}
		if rule.When.Kind != "" {
			entry["if"] = predicateSchema(rule.When)
		}
		allOf = append(allOf, entry)
	}
	schema := map[string]any{
		"$schema":              "https://json-schema.org/draft/2020-12/schema",
		"$id":                  "https://leapview.dev/schemas/config/environment.schema.json",
		"title":                "LeapView process-global environment",
		"type":                 "object",
		"additionalProperties": true,
		"properties":           properties,
		"allOf":                allOf,
	}
	encoded, err := json.MarshalIndent(schema, "", "  ")
	if err != nil {
		return nil, err
	}
	return append(encoded, '\n'), nil
}

func predicateSchema(predicate configspec.Predicate) any {
	switch predicate.Kind {
	case configspec.PredicateAll:
		children := make([]any, 0, len(predicate.Children))
		for _, child := range predicate.Children {
			children = append(children, predicateSchema(child))
		}
		return map[string]any{"allOf": children}
	case configspec.PredicateAny:
		children := make([]any, 0, len(predicate.Children))
		for _, child := range predicate.Children {
			children = append(children, predicateSchema(child))
		}
		return map[string]any{"anyOf": children}
	case configspec.PredicateNot:
		if len(predicate.Children) != 1 {
			return false
		}
		return map[string]any{"not": predicateSchema(predicate.Children[0])}
	case configspec.PredicatePresent:
		return map[string]any{"required": []string{predicate.Name}, "properties": map[string]any{predicate.Name: map[string]any{"minLength": 1}}}
	case configspec.PredicateTrue:
		return map[string]any{"required": []string{predicate.Name}, "properties": map[string]any{predicate.Name: map[string]any{"enum": []string{"1", "t", "T", "true", "TRUE", "True"}}}}
	case configspec.PredicateMinLength:
		return map[string]any{"required": []string{predicate.Name}, "properties": map[string]any{predicate.Name: map[string]any{"minLength": predicate.Minimum}}}
	case configspec.PredicateHTTPSURL:
		return map[string]any{"required": []string{predicate.Name}, "properties": map[string]any{predicate.Name: map[string]any{"pattern": "^https://[^/]+"}}}
	case configspec.PredicateHTTPSOrigin:
		return map[string]any{"required": []string{predicate.Name}, "properties": map[string]any{predicate.Name: map[string]any{"pattern": `^https://[^/@?#]+/?$`}}}
	case configspec.PredicateSlug:
		return map[string]any{"properties": map[string]any{predicate.Name: map[string]any{"maxLength": 64, "pattern": "^[A-Za-z0-9][A-Za-z0-9._-]*$"}}}
	case configspec.PredicateEquals:
		return map[string]any{"required": []string{predicate.Name}, "properties": map[string]any{predicate.Name: map[string]any{"const": predicate.Value}}}
	case configspec.PredicateOneOf:
		return map[string]any{"required": []string{predicate.Name}, "properties": map[string]any{predicate.Name: map[string]any{"enum": predicate.Values}}}
	case configspec.PredicatePositive:
		// Defaults are applied by the runtime decoder, so sparse environment
		// documents must not be forced to repeat every positive default.
		return map[string]any{"x-runtime-positive": predicate.Name}
	case configspec.PredicateAtLeast:
		// JSON Schema cannot compare two environment-string properties without
		// non-standard extensions; runtime validation enforces this relationship.
		return map[string]any{"x-runtime-at-least": []string{predicate.Name, predicate.OtherName}}
	default:
		return true
	}
}

func title(value string) string {
	words := strings.Fields(strings.ReplaceAll(value, "-", " "))
	for index := range words {
		words[index] = strings.ToUpper(words[index][:1]) + words[index][1:]
	}
	return strings.Join(words, " ")
}

func fatal(err error) {
	fmt.Fprintln(os.Stderr, err)
	os.Exit(1)
}
